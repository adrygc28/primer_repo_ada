// Operadores aritmeticos

let a = 10;
let b = 3;
console.log("Suma:", a + b); // Suma: 13
console.log("Resta:", a - b); // Resta: 7
console.log("Multiplicación:", a * b); // Multiplicación: 30
console.log("División:", a / b); // División: 3.3333...
console.log("Módulo:", a % b); // Módulo: 1


// Operadores de comparación
console.log(a > b); // a es Mayor que b (>): true
console.log(a < b); // a es Menor que b (<): false
console.log(a === "10"); // False a es numero y "10" es cadena de texto

// Operadores lógicos
console.log(true && false);
// && (AND): true si ambos son verdaderos
console.log(true || false);
// || (OR): true si al menos uno es verdadero
console.log(!true); 
// ! (NOT): invierte el valor booleano