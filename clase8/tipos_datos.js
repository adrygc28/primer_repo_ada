// Definimos diferentes tipos de datos
let numero = 42; // Número
let texto = "Hola, mundo"; // Cadena de texto
let booleano = true; // Booleano (verdadero/falso)
let indefinido; // Indefinido - Variable no inicializada
let nulo = null; // Nulo nulo (vacio)
let objeto = { nombre: "Jack", edad: 8}; // Objeto con propiedades


// Mostrar los valores y explicamos su tipo
console.log("Numero", numero); // Número
console.log("Texto", texto); // Cadena de texto
console.log("Booleano", booleano); // Booleano
console.log("Indefinido", indefinido);  // Indefinido
console.log("Nulo", nulo);  // Nulo                 
console.log("Objeto", objeto); // Objeto
     
